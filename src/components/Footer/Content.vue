        });
      }
    },
    toggle(index) {
      let _checker = false;
      for (let i = 0, a = this.accordionList; i < a.length; i++) {
        if (index === a[i]) {
          this.accordionList.splice(i, 1);
          _checker = true;
          break;
        }
      }

      if (!_checker) this.accordionList.push(index);
    }
